    private void virtualKeyboardActionPerformed(java.awt.event.ActionEvent evt) {
        fireActionPerformed(evt);
        ((JButton)evt.getSource()).setEnabled(false);
    }
    
    public void addActionListener(ActionListener actionListener) {//Event Management
        actionListenerList.add(ActionListener.class, actionListener);
    }
 
    public void removeActionListener(ActionListener actionListener) {
      actionListenerList.remove(ActionListener.class, actionListener);
    }
 
    protected void fireActionPerformed(ActionEvent actionEvent) {//trigger event
        EventListener listenerList[] = actionListenerList.getListeners(ActionListener.class);
        for (int i = 0, n = listenerList.length; i < n; i++) {
          ((ActionListener) listenerList[i]).actionPerformed(actionEvent);
        }
    }
    
    public void markAsTriggered(String letter) {
        var components = this.getComponents();
        
        for(int i = 0; i < components.length; i++) {
            var component = components[i];
            if(component instanceof JButton && ((JButton)component).getText().equals(letter.toUpperCase())) {
                components[i].setEnabled(false);
            }
        }
    }
    
    public void reset() {
        setEnabled(true);
    }
    
    @Override
    public void setEnabled(boolean enabled) {
        super.setEnabled(enabled); //To change body of generated methods, choose Tools | Templates.
        
        var components = this.getComponents();
        
        for(int i = 0; i < components.length; i++) {
            if(components[i] instanceof JButton) {
                components[i].setEnabled(enabled);
            }
        }
    }
